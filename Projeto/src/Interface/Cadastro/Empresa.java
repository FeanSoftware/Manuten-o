    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Setor empresa = new Setor();
        if (!jTextCNPJ.getText().equals("") && !jTextNome.getText().equals("")) {
            if (JOptionPane.showConfirmDialog(null, "Deseje salvar os dados\nEmpresa: " + jTextNome.getText() + "\nCNPJ: " + jTextCNPJ.getText(), "Deseja mesmo salvar", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                empresa.setCNPJ(jTextCNPJ.getText());
                empresa.setNome(jTextNome.getText());
                InserirDados.inserirEmpresa(empresa);
                jTextCNPJ.setText("");
                jTextNome.setText("");
            }
        }

    }//GEN-LAST:event_jButtonSalvarActionPerformed

